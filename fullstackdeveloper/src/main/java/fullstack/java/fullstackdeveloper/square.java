package fullstack.java.fullstackdeveloper;

public class square 
{
	public static void main(String[] args) 
	{
		System.out.println("Printing Pattern Square");
	for(int row=1,alpha=1;row<=3;row++)
	{
		for(int col=1;col<=5;col++,alpha++)
		{
			System.out.print(alpha+" ");
		}
		System.out.println();
	}
	}
}
	
